class Postre {
    constructor(
        id = null,
        name = '',
        calories = 0,
        fat = 0,
        carbs = 0,
        protein = 0,
        iron = 0,
   ) {
    this.id = id;
    this.name = name;
    this.calories = calories;
    this.fat = fat;
    this.carbs = carbs;
    this.protein = protein;
    this.iron = iron;
    }
  
}

export default Postre;